// DAILY METRICS (last 30 days)
// ============================================================

function generateDailyMetrics(): DailyMetrics[] {
  const metrics: DailyMetrics[] = []
  for (let i = 29; i >= 0; i--) {
    const date = new Date(Date.now() - i * 24 * 60 * 60 * 1000)
    const abandoned = Math.floor(Math.random() * 30) + 20
    const contacted = Math.floor(abandoned * (0.6 + Math.random() * 0.2))
    const recovered = Math.floor(contacted * (0.15 + Math.random() * 0.15))
    const paid = Math.floor(recovered * (0.7 + Math.random() * 0.2))
    const avgTicket = 250 + Math.random() * 200
    const abandonedValue = abandoned * avgTicket
    const recoveredValue = recovered * avgTicket
    const paidValue = paid * avgTicket

    const sent = contacted * 3
    const delivered = Math.floor(sent * 0.95)
    const read = Math.floor(delivered * (0.6 + Math.random() * 0.2))
    const replied = contacted
    const clicks = Math.floor(contacted * (0.2 + Math.random() * 0.15))
    const discountRecs = Math.floor(recovered * 0.3)

    metrics.push({
      id: `metric-${i}`,
      storeId: 'all',
      date: date.toISOString().split('T')[0],
      abandonedCount: abandoned,
      abandonedValue,
      contactedCount: contacted,
      recoveredCount: recovered,
      recoveredValue,
      paidCount: paid,
      paidValue,
      avgTicket: Math.round(avgTicket * 100) / 100,
      recoveryRate: contacted > 0 ? Math.round((recovered / contacted) * 100 * 10) / 10 : 0,
      totalConversations: contacted,
      avgMessagesPerConv: 3 + Math.random() * 4,
      aiCost: contacted * 0.01,
      messagesSent: sent,
      messagesDelivered: delivered,
      messagesRead: read,
      messagesReplied: replied,
      linkClicks: clicks,
      discountRecoveries: discountRecs,
      avgDiscountPercent: discountRecs > 0 ? 8 + Math.random() * 4 : 0,
      createdAt: date.toISOString(),
    })
  }
  return metrics
}

export const MOCK_DAILY_METRICS = generateDailyMetrics()

// ============================================================
// DASHBOARD DATA
// ============================================================

export function getDashboardData(period: string = '30d'): DashboardData {
  const days = period === '7d' ? 7 : period === '90d' ? 90 : 30
  const metrics = MOCK_DAILY_METRICS.slice(-days)

  const totalAbandoned = metrics.reduce((sum, m) => sum + m.abandonedCount, 0)
  const totalAbandonedValue = metrics.reduce((sum, m) => sum + m.abandonedValue, 0)
  const totalContacted = metrics.reduce((sum, m) => sum + m.contactedCount, 0)
  const totalRecovered = metrics.reduce((sum, m) => sum + m.recoveredCount, 0)
  const totalRecoveredValue = metrics.reduce((sum, m) => sum + m.recoveredValue, 0)
  const totalPaid = metrics.reduce((sum, m) => sum + m.paidCount, 0)
  const totalPaidValue = metrics.reduce((sum, m) => sum + m.paidValue, 0)
  const totalAiCost = metrics.reduce((sum, m) => sum + m.aiCost, 0)

  const totalSent = metrics.reduce((sum, m) => sum + m.messagesSent, 0)
  const totalDelivered = metrics.reduce((sum, m) => sum + m.messagesDelivered, 0)
  const totalRead = metrics.reduce((sum, m) => sum + m.messagesRead, 0)
  const totalReplied = metrics.reduce((sum, m) => sum + m.messagesReplied, 0)
  const totalClicks = metrics.reduce((sum, m) => sum + m.linkClicks, 0)

  return {
    totalAbandoned,
    totalAbandonedValue,
    totalContacted,
    totalRecovered,
    totalRecoveredValue,
    totalPaid,
    totalPaidValue,
    recoveryRate: totalContacted > 0 ? (totalRecovered / totalContacted) * 100 : 0,
    avgTicket: totalRecovered > 0 ? totalRecoveredValue / totalRecovered : 0,
    totalConversations: totalContacted,
    avgMessagesPerConv: 4.5,
    totalAiCost: totalAiCost,
    responseRate: totalSent > 0 ? (totalReplied / totalSent) * 100 : 0,
    openRate: totalDelivered > 0 ? (totalRead / totalDelivered) * 100 : 0,
    clickRate: totalSent > 0 ? (totalClicks / totalSent) * 100 : 0,
    costPerRecovery: totalRecovered > 0 ? totalAiCost / totalRecovered : 0,
    dailyMetrics: metrics,
  }
}
